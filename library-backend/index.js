const typeDefs = `
  type Book {
    title: String!
    published: Int!
    author: Author! # Ahora devuelve el objeto Author completo
    id: ID!
    genres: [String!]!
  }

  type Author {
  name: String!
  id: ID!
  born: Int
  bookCount: Int!
}

  type Query {
    bookCount: Int!
    authorCount: Int!
    allBooks(author: String, genre: String): [Book!]!
    allAuthors: [Author!]!
  }

  type Mutation {
    addBook(
      title: String!
      author: String!
      published: Int!
      genres: [String!]!
    ): Book
    editAuthor(
      name: String!
      setBornTo: Int!
    ): Author
  }
`;

const resolvers = {
  Query: {
    bookCount: async () => Book.collection.countDocuments(),
    authorCount: async () => Author.collection.countDocuments(),
    allBooks: async () => {
      // El enunciado dice que el campo author de un libro no tiene que funcionar todavía,
      // pero para que no de error, devolvemos los libros.
      return Book.find({});
    },
    allAuthors: async () => Author.find({}),
  },

  Book: {
    // Como en la BD 'author' es un ID, buscamos el objeto Author
    author: async (root) => {
      return Author.findById(root.author);
    },
  },

  Mutation: {
    addBook: async (root, args) => {
      // 1. Buscamos si el autor ya existe por su nombre
      let author = await Author.findOne({ name: args.author });

      // 2. Si no existe, lo creamos
      if (!author) {
        author = new Author({ name: args.author });
        await author.save();
      }

      // 3. Creamos el libro usando el ID del autor encontrado/creado
      const book = new Book({ ...args, author: author._id });
      return book.save();
    },
    // editAuthor se queda pendiente según el enunciado
  },
};

const server = new ApolloServer({
  typeDefs,
  resolvers,
});

startStandaloneServer(server, {
  listen: { port: 4000 },
}).then(({ url }) => {
  console.log(`Server ready at ${url}`);
});
